<section class="mx-auto max-w-2xl flex flex-col gap-4">
	<h2 class="text-2xl font-bold font-mono">Starting Your Day</h2>
	<p>The way we start our day for work, personal or anything really sets the tone. I've found the best way for me to kick off my WFH day is to treat is as if I'm going into the office to work around other people.</p>
	<p>I know, I know this sounds insane right? Let me breakdown what this means for me.</p>
	<ol class="list-decimal p-4 flex flex-col gap-2">
