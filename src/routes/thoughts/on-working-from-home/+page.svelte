	<h2 id="morning-routine" class="text-xl font-bold font-mono">Keep a morning routine</h2>
	<p>Too many people wait until the last minute to start work before they actually wake up. For me this does not work. I need to keep a morning routine. So that means a simple shower, brush your teeth and eat a breakfast. This simple routine wakes me up, gives me energy and I'm sure gives my body a good jolt of energy to get started.</p>

	<h2 id="dress-progress" class="text-xl font-bold font-mono">Dress for Progress</h2>
	<p>I say this a lot but please dress in some kind of normal clothes. Ditch the pajamas and sleep clothes. You certainly don't need a full suit or polo but think of your home as Everyday Jeans Friday. The point is you want to take yourself out of sleep mode to be more productive and active. For me it's putting on actual clothes that helps me with this mindset.</p>

	<h2 id="fresh-air" class="text-xl font-bold font-mono">Fresh Air</h2>
	<p>Being stuck in the house all day is not always best for mental health. So what I've been doing before I start my day is open up the blinds and curtains. If the weather is calm I'll open up the windows. Getting some fresh air and light inside helps me 10x more. It feels great to have some natural light and air in my home while I'm clicking away at a keyboard.</p>

	<h2 id="my-workspace" class="text-xl font-bold font-mono">My Workspace</h2>
	<p>I think it is very, very important for people to have defined areas of what is work and what is just home. For some people, that's an office that can be seperated from the rest of the house. Others don't have that luxury. In my case, I've setup a desk space that is dedicated for me to work at. It's off to the side of my living room but it works great. I have a dedicated space to get into work mode and when I'm done I take the short commute to my couch to relax and enjoy family time.</p>

	<h2 id="define-schedule" class="text-xl font-bold font-mono">Define Your Schedule</h2>
	<p>My final point of WFH productiveness is to define your work schedule. Now your company might make this easier. They might say we work from 9 to 5 or 8 to 4 and that's actually a benefit. The important part is you stick to that schedule. What 4/5 pm comes you put some small finishing touches on what you're working on and log off. You're at home and it's too easy to get sucked into "always work mode". The problem is that is not healthy. You need strict schedules of what you're working and when you're not just like you're at the office.</p>
</section>
